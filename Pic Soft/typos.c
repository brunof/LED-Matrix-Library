int1 ProximoElementoLetra(void)
{
   if(letraPosicionActual >= letraFinal) return FALSE;
   letraData = DataLetras[letraPosicionActual++]; 
   return TRUE;
}


void CargarLetra(char CodigoLetra)
{
   letraFinal = IndiceTypos[typoActual];           //obtener direccion de comienzo de la tipografia actual dentro de IndiceLetras[]
   letraFinal+= CodigoLetra;                       //sumarle el valor ASCII de la letra a cargar para obtener la posicion de la letra dentro de IndiceLetras[] 
   letraPosicionActual= IndiceLetras[letraFinal];  // leer el elemento de IndiceLetras[] para obtener la posicion inicial de la tipografia  
   letraFinal= IndiceLetras[++letraFinal];         // leer el elemento de IndiceLetras[] para obtener la posicion final de la tipografia     
}

